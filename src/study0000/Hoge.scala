package study0000

class Hoge {
  // 基本型におけるデフォルト値での初期化。
  // _ はデフォルト値を意味する。 
  // Scalaには int 等のプリミティブ型は無い。
  // 修飾子は書いていないので、Javaで言うところのpublic扱い。
  var vChar: Char = _;
  var vByte: Byte = _;
  var vShort: Short = _;
  var vInt: Int = _;
  var vLong: Long = _;
  var vFloat: Float = _;
  var vDouble: Double = _;
  var vBoolean: Boolean = _;
  var vString: String = _;
}